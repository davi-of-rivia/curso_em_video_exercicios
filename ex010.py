# Crie um programa que leia a quantidade de dinheiro que uma pessoa tem na carteira e mostre quanto que ela pode comprar
dinheiro = float(input('Quantos reais você tem na carteira? '))
print(f'{(dinheiro / 4.29):.2f} doláres')
print(f'{(dinheiro / 4.66):.2f} euros')
print(f'{(dinheiro / 5.6):.2f} libras esterlinas    ')
print(f'{(dinheiro / 0.039):.2f} ienes')
